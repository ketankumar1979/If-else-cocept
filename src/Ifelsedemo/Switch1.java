package Ifelsedemo;

public class Switch1 {
    public static void main (String[]args){
        int a=10;

        switch (a){
            case 0:
                System.out.println("zero");

            case 5:
                System.out.println("five");
            case 10:
                System.out.println("ten");
            case 11:
                System.out.println("eleven");
                break;

        }
    }
}
